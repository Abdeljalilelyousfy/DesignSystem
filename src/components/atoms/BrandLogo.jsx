export const BRAND_NAMES = Object.keys(BRAND_IMAGES)

// ─── Size scale ───────────────────────────────────────────────────────────────
const SIZES = {
  xs:  { tile: 64,  padding: 8  },
  sm:  { tile: 80,  padding: 10 },
  md:  { tile: 110, padding: 14 },
  lg:  { tile: 140, padding: 18 },
  xl:  { tile: 180, padding: 24 },
}

// ─── Component ────────────────────────────────────────────────────────────────
/**
 * BrandLogo — 20-make automotive logo tile
 *
 * make    string   — one of BRAND_NAMES
 * type    color | black | white   (default: color)
 * size    xs | sm | md | lg | xl  (default: md)
 * radius  number   — border-radius in px (default: 8)
 * label   boolean  — show make name below tile (default: false)
 */
export default function BrandLogo({
  make,
  type = 'color',
  size = 'md',
  radius = 8,
  label = false,
  style,
}) {
  const { tile, padding } = SIZES[size] || SIZES.md
  const imgSrc = BRAND_IMAGES[make]?.[type === 'white' ? 'black' : type]

  const bg =
    type === 'white' ? 'var(--color-bg-inverse)' :
    type === 'black' ? 'var(--color-bg-hover)'   :
    'var(--color-bg-primary)'

  const border =
    type === 'white' ? 'none' :
    '1px solid var(--color-border-tertiary)'

  const imgFilter = type === 'white' ? 'brightness(0) invert(1)' : 'none'

  return (
    <div style={{ display: 'inline-flex', flexDirection: 'column', alignItems: 'center', gap: 8, ...style }}>
      <div style={{
        width: tile, height: tile,
        borderRadius: radius,
        padding,
        background: bg,
        border,
        display: 'flex', alignItems: 'center', justifyContent: 'center',
        boxSizing: 'border-box',
        flexShrink: 0,
        overflow: 'hidden',
      }}>
        {imgSrc ? (
          <img
            src={imgSrc}
            alt={make}
            style={{
              width: '100%',
              height: '100%',
              objectFit: 'contain',
              filter: imgFilter,
              display: 'block',
            }}
          />
        ) : (
          <span style={{
            fontFamily: 'Inter', fontSize: 10, fontWeight: 500,
            color: type === 'white' ? '#fff' : 'var(--color-content-tertiary)',
            textAlign: 'center', lineHeight: 1.2,
          }}>
            {make}
          </span>
        )}
      </div>
      {label && (
        <span style={{
          fontFamily: 'Inter', fontSize: 11, fontWeight: 400,
          color: 'var(--color-content-tertiary)',
          textAlign: 'center', maxWidth: tile, lineHeight: 1,
        }}>
          {make}
        </span>
      )}
    </div>
  )
}
